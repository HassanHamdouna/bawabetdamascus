  String get accountant;

  /// No description provided for @manager.
  ///
  /// In en, this message translates to:
  /// **'Manager'**
  String get manager;

  /// No description provided for @system_managers.
  ///
  /// In en, this message translates to:
  /// **'System Managers'**
  String get system_managers;

  /// No description provided for @owner.
  ///
  /// In en, this message translates to:
